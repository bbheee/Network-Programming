CREATE TABLE `CURRENCY`(
id INT NOT NULL,
country VARCHAR(3) NOT NULL
);


CREATE TABLE `EXCHANGE_RATE`(
id INT PRIMARY KEY AUTO_INCREMENT,
fromCountry INT NOT NULL FOREIGN KEY REFERENCES CURRENCY(id),
toCountry INT NOT NULL FOREIGN KEY REFERENCES CURRENCY(id),
rate DOUBLE NOT NULL
);


